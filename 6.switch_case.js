// SWITCH - CASE

const opcao = 3 

switch (opcao) {
    case 1:
        console.log("Opção 1 selecionada");
        break;  
    case 2:
        console.log("Opção 2 selecionada");
        break;
    case 3:
        console.log("Opção 3 selecionada");
        break;
    case 4:
        console.log("Opção 4 selecionada");
        break;
    case 5:
        console.log("Opção 5 selecionada");
        break;
    default:
        console.log("Opção inválida");
}



    
    
 
        








