// Criando um vetor

const listaNomes = ("Marta", "Julia","Ana")
console.log("\nExibindo todos os nomes")
console.log(listaNomes)

console.log("\nExibindo o primeiro elemento: ")
console.log(listaNomes[0])

console.log("\nExibindo o segundo elemento: ")
console.log(listaNomes[1])

console.log("\nExibindo o terceiro elemento: ")
console.log(listaNomes[2])

console.log("\n Adicionando um elemento: ")
listaNomes.push ("Mariana")
console.log (listaNomes)

console.log("\n Removendo o primeiro elemento")
listaNomes.shift()
console.log(listaNomes)

console.log("\n Removendo o ultimo elemento")
listaNomes.pop()
console.log(listaNomes)

console.log("\n Removendo um elemento especifico: ")
listaNomes.split(1,1)
console.log(listaNomes)